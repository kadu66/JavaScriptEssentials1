// 1. Declarando variáveis
let minhaString = "Aureliano";
let meuNumero = 42;
let meuBooleano = true;

console.log(minhaString, typeof minhaString); 
console.log(meuNumero, typeof meuNumero);    
console.log(meuBooleano, typeof meuBooleano); 

// 2. Número para String
let numeroParaString = String(meuNumero);
console.log(numeroParaString, typeof numeroParaString); 
// 3. String numérica para número
let stringNumerica = "123";
let stringParaNumero = Number(stringNumerica);
console.log(stringParaNumero, typeof stringParaNumero); 